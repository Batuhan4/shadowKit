/**
 * Gemini structured-output schema for the ActionPlan. Passed via
 * `config.responseSchema` together with `config.responseMimeType: "application/json"`
 * (verified: @google/genai, ctx7 /googleapis/js-genai "Generate Structured JSON Output").
 * Uses the SDK `Type` enum (Type.OBJECT / Type.STRING). `propertyOrdering` keeps the model's
 * key order stable; `required` constrains the three fields. The model returns JSON matching
 * this schema in `response.text` (and in the accumulated stream chunk text).
 */
export const ACTION_PLAN_SCHEMA = {
  type: Type.OBJECT,
  properties: {
    amountIn: { type: Type.STRING },
    minOut: { type: Type.STRING },
    reasoning: { type: Type.STRING },
  },
  required: ["amountIn", "minOut", "reasoning"],
  propertyOrdering: ["amountIn", "minOut", "reasoning"],
} as const;

/**
 * Per-request user message — carries ALL volatile data (cap, spec, market). Kept OUT of the
 * frozen system instruction so the system instruction stays byte-stable (maximises Gemini
 * implicit caching). Deterministic for identical inputs.
 */
export function buildUserMessage(spec: ActionSpec, cap: string, market: MarketData): string {
  return [
    "Approved swap to execute:",
    `- input asset (assetIn): ${spec.assetIn}`,
    `- output asset (assetOut, the swap target): ${spec.assetOut}`,
    `- proposal cap (max amountIn, smallest unit): ${cap}`,
    `- proposal minOut floor (smallest unit): ${spec.minOut}`,
    "",
    "Current market data:",
    `- pair: ${market.pair}`,
    `- price: ${market.price}`,
    `- signal: ${market.signal}`,
    "",
    "Produce the execution plan now: choose amountIn (<= cap) and minOut (>= floor),",
    "and explain your reasoning briefly.",
  ].join("\n");
}
